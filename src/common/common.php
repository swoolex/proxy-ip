/**
 * PHP时间戳人性化转换成多少天前类的格式
 * @todo 无
 * @author 小黄牛
 * @version v1.2.16 + 2020.10.27
 * @deprecated 暂不启用
 * @global 无
 * @param int $time 时间戳
 * @return string
*/
function formatTime($time){
    $t = time()-$time;
    $f = [
        '31536000'=>'年',
        '2592000'=>'个月',
        '604800'=>'星期',
        '86400'=>'天',
        '3600'=>'小时',
        '60'=>'分钟',
        '1'=>'秒'
    ];
    foreach ($f as $k=>$v)    {
        if (0 !=$c=floor($t/(int)$k)) {
            return $c.$v.'前';
        }
    }
    return '0 秒前';
}